def wiki_category_titles(n):
    """Article titles from measurement-dense categories, round-robin."""
    per_cat = {}
    dropped = []
    for cat in CATEGORIES:
        # The sleep goes BEFORE the request, not after. It used to sit at the end
        # of the loop body, where `continue` skipped it -- so the instant a
        # category 429'd, the next one was fired with no delay at all. A rate
        # limit producing MORE requests is a positive feedback loop, and it
        # silently shrank the corpus while printing a line that looked like a
        # harmless note. Same family as the &nbsp; bug: a quiet under-count that
        # comes back looking fine. (fire 143)
        #
        # 2.0 was the bug. The API edge publishes `retry-after: 3`, so 2.0 sat
        # permanently inside the refusal window and 429'd all sixty categories.
        # Measured fire 144: 4.0s spacing -> 6/6 requests 200. fetch_json now
        # honors Retry-After as well, so this is the floor, not the safety net.
        time.sleep(REQ_SPACING)
        url = ("https://en.wikipedia.org/w/api.php?action=query&format=json"
               "&list=categorymembers&cmnamespace=0&cmlimit=500&cmtitle="
               + urllib.parse.quote(cat))
        try:
            members = fetch_json(url)["query"]["categorymembers"]
        except Exception as e:
            print(f"  (category {cat} failed: {e})", file=sys.stderr)
            dropped.append(cat)
            continue
        per_cat[cat] = [m["title"] for m in members]
    if dropped:
        # A dropped category is not a cosmetic loss -- it is a sample that is not
        # the sample I say it is. Say so loudly rather than reporting a rate over
        # a corpus quietly different from the described one.
        print(f"  !! {len(dropped)}/{len(CATEGORIES)} categories DROPPED: "
              f"{', '.join(c.replace('Category:', '') for c in dropped)}", file=sys.stderr)
    # round-robin so no single category dominates the sample
    titles, i = [], 0
    while len(titles) < n and any(len(v) > i for v in per_cat.values()):
        for v in per_cat.values():
            if len(v) > i and len(titles) < n:
                titles.append(v[i])
        i += 1
    print(f"  drew from {len(per_cat)} categories")
    return titles[:n]


def fetch_json(url, tries=8):
    """Fetch, letting the SERVER set the pace.

    Fire 143 diagnosed the 429 storm as "Wikipedia is rate-limiting hard" and
    queued the fix "back off harder." Both were guesses. Fire 144 read the whole
    refusal instead of its status line:

        HTTP/2 429
        x-envoy-ratelimited: true
        retry-after: 3

    The number was in the response the entire time, in a header named
    `Retry-After`, and the loop was sleeping 2.0 -- one second short, forever.
    A request arriving inside the window doesn't merely get refused, it keeps
    the bucket empty, so a too-fast caller 429s on EVERY request and it looks
    identical to a permanent IP block. (It looked exactly like that: sixty of
    sixty categories, hours after the previous run, even on an empty
    `meta=siteinfo` query.)

    So: no guessed constant. Obey `Retry-After` when it is sent; fall back to
    exponential backoff only when it isn't. A constant that happens to clear a
    published threshold is a fix that breaks silently the day they change it.
    """
    delay = 2.0
    for attempt in range(tries):
        req = urllib.request.Request(url, headers={"User-Agent": UA})
        try:
            with urllib.request.urlopen(req, timeout=60) as r:
                return json.loads(r.read().decode("utf-8"))
        except urllib.error.HTTPError as e:
            if e.code in (429, 503) and attempt < tries - 1:
                # The server's own number wins over anything I would pick.
                ra = e.headers.get("Retry-After") if e.headers else None
                try:
                    wait = float(ra) + 0.5
                except (TypeError, ValueError):
                    wait = delay
                    delay *= 2
                time.sleep(wait)
                continue
            raise
    raise RuntimeError("unreachable")


CACHE = os.path.join(os.path.dirname(os.path.abspath(__file__)), "cache")


def cached_wikitext(titles):
    """Fetch, but never fetch the same article twice across runs."""
    os.makedirs(CACHE, exist_ok=True)
    out, missing = {}, []
    for tt in titles:
        fn = os.path.join(CACHE, hashlib.sha1(tt.encode()).hexdigest() + ".txt")
        if os.path.exists(fn):
            out[tt] = open(fn, encoding="utf-8").read()
        else:
            missing.append(tt)
    if missing:
        print(f"  {len(out)} cached, fetching {len(missing)}…")
        fresh = fetch_wikitext(missing)
        for tt, wt in fresh.items():
            fn = os.path.join(CACHE, hashlib.sha1(tt.encode()).hexdigest() + ".txt")
            with open(fn, "w", encoding="utf-8") as fh:
                fh.write(wt)
        out.update(fresh)
    return out


def fetch_wikitext(titles):
    """Up to 50 titles per call. Returns {title: wikitext}."""
    out = {}
    for i in range(0, len(titles), 25):
        chunk = titles[i:i + 25]
        url = ("https://en.wikipedia.org/w/api.php?action=query&format=json"
               "&prop=revisions&rvprop=content&rvslots=main&titles="
               + urllib.parse.quote("|".join(chunk)))
        try:
            data = fetch_json(url)
        except Exception as e:
            print(f"  (fetch failed for a chunk: {e})", file=sys.stderr)
            continue
        for page in data.get("query", {}).get("pages", {}).values():
            try:
                out[page["title"]] = page["revisions"][0]["slots"]["main"]["*"]
            except (KeyError, IndexError):
                pass
        # Was 1.0 -- inside the published 3s refusal window, same bug as the
        # category loop. Every chunk boundary was a coin-flip on a 429 that
        # `continue` then swallowed as a silently missing 25 articles.
        time.sleep(REQ_SPACING)
    return out


def strip_convert_templates(text):
    """Remove {{convert|...}} / {{cvt|...}} calls, brace-balanced.

    Everything left is a dual-unit pair a human typed. Returns
    (text_without_templates, number_of_templates_removed).
    """
    out = []
    i = 0
    removed = 0
    while True:
        m = CONVERT_RE.search(text, i)
        if not m:
            out.append(text[i:])
            break
        out.append(text[i:m.start()])
        depth = 0
        j = m.start()
        while j < len(text):
            if text.startswith("{{", j):
                depth += 1
                j += 2
            elif text.startswith("}}", j):
                depth -= 1
                j += 2
                if depth == 0:
                    break
            else:
                j += 1
        removed += 1
        i = j
    return "".join(out), removed


def render_convert_templates(text):
    """Turn {{convert|A|unitA|unitB}} into 'A unitA (B unitB)' as the template
    itself would -- but computing B correctly. Used to build the CONTROL group:
    pairs that are right by construction. If the checker flags these, it is broken.
    """
    pairs = []
    for m in re.finditer(r"\{\{\s*(?:convert|cvt)\s*\|([^{}]*)\}\}", text, re.IGNORECASE):
        parts = [p.strip() for p in m.group(1).split("|")]
        if len(parts) < 3:
            continue
        a_num, a_unit, b_unit = parts[0], parts[1], parts[2]
        if not re.fullmatch(NUM, a_num):
            continue
        au, bu = a_unit.lower(), b_unit.lower()
        if au not in UNITS or bu not in UNITS or UNITS[au][0] != UNITS[bu][0]:
            continue
        exact = parse_num(a_num) * UNITS[au][1] / UNITS[bu][1]
        # the template rounds to comparable precision; emulate with 3 s.f.
        b_num = f"{exact:.3g}"
        pairs.append((a_num, a_unit, b_num, b_unit))
    return pairs


def main():
    if not gates():
        raise SystemExit("gates failed — the detector is not trustworthy, no verdict given")

    n = int(sys.argv[1]) if len(sys.argv) > 1 else 400
    print(f"── corpus: up to {n} English Wikipedia articles, measurement-dense categories ──")
    titles = wiki_category_titles(n)
    pages = cached_wikitext(titles)
    print(f"  fetched wikitext for {len(pages)} articles\n")

    control = {"CLOSES": 0, "LOOSE": 0, "MISMATCH": 0, "SKIP": 0}
    treat = {"CLOSES": 0, "LOOSE": 0, "MISMATCH": 0, "SKIP": 0}
    findings = []
    n_templates = 0
    n_compound = 0

    for title, text in pages.items():
        # CONTROL: pairs the software generated
        for a, au, b, bu in render_convert_templates(text):
            v, _ = check_pair(a, au, b, bu)
            control[v] += 1

        # TREATMENT: what a person typed, with the templates cut out first
        human_text, removed = strip_convert_templates(text)
        n_templates += removed
        for m in PAIR_RE.finditer(human_text):
            if is_compound(human_text, m.start()):
                treat["SKIP"] += 1
                n_compound += 1
                continue
            v, detail = check_pair(m.group("a_num"), m.group("a_unit"),
                                   m.group("b_num"), m.group("b_unit"))
            treat[v] += 1
            if v == "MISMATCH":
                ctx = human_text[max(0, m.start() - 90):m.end() + 90].replace("\n", " ")
                findings.append({"article": title, "detail": detail, "context": ctx})

    def rate(d):
        n = d["CLOSES"] + d["LOOSE"] + d["MISMATCH"]
        return (100.0 * d["MISMATCH"] / n) if n else float("nan"), n

    c_rate, c_n = rate(control)
    t_rate, t_n = rate(treat)

    print("── CONTROL: conversions generated by {{convert}} ──")
    print(f"  {c_n} checkable pairs, {control['MISMATCH']} flagged  ->  {c_rate:.2f}%")
    print("  (this is the gate. it must be ~0. any flag here is MY bug, not Wikipedia's.)\n")
    print("── TREATMENT: conversions typed by a person ──")
    print(f"  {t_n} checkable pairs, {treat['MISMATCH']} flagged  ->  {t_rate:.2f}%\n")
    print(f"  ({n_templates} convert-templates removed before scanning the human text;")
    print(f"   {n_compound} compound measurements like '6 ft 3 in (1.91 m)' skipped)")

    if findings:
        print("\n── every mismatch, so it can be checked by hand ──")
        for f in findings:
            print(f"\n  {f['article']}")
            print(f"    printed : {f['detail']['printed']}")
            print(f"    true    : {f['detail']['should_be']} "
                  f"(printed/true = {f['detail']['ratio_printed_over_true']})")
            print(f"    context : …{f['context'].strip()[:180]}…")

    out = {
        "control": control, "treatment": treat,
        "control_rate_pct": c_rate, "treatment_rate_pct": t_rate,
        "articles": len(pages), "findings": findings,
    }
    with open("seam_result.json", "w") as fh:
        json.dump(out, fh, indent=2, default=str)
    print("\nwrote seam_result.json")


