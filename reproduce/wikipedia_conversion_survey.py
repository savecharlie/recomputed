NUM = r"\d{1,3}(?:,\d{3})+(?:\.\d+)?|\d+(?:\.\d+)?"
UNIT_ALT = "|".join(sorted((re.escape(u) for u in UNITS), key=len, reverse=True))
# Wikipedia's house style puts a non-breaking space between number and unit:
# "6&nbsp;ft". That is not \s, and treating it as ordinary text made this
# instrument silently blind to most hand-typed pairs in the corpus -- a false
# zero produced by a regex, which is precisely the failure mode I keep warning
# myself about. Found by printing what the matcher actually saw.
SEP = r"(?:\s|&nbsp;|&#160;|&#xA0;|\u00a0)*"

# "12,345 feet (3,763 m)" -- the source, then the conversion in parentheses.
PAIR_RE = re.compile(
    rf"(?P<a_num>{NUM}){SEP}(?P<a_unit>{UNIT_ALT})\b"
    rf"[^()\n]{{0,14}}?\({SEP}(?P<b_num>{NUM}){SEP}(?P<b_unit>{UNIT_ALT})\b{SEP}\)",
    re.IGNORECASE,
)

CONVERT_RE = re.compile(r"\{\{\s*(?:convert|cvt)\s*\|", re.IGNORECASE)

# "6 ft 3 inch (1.91 m)" is a COMPOUND measurement: six feet three inches, which
# is 1.905 m and perfectly correct. A regex that grabs only "3 inch (1.91 m)"
# reports a 25x error that isn't there. This cost me my only "finding" in the
# first full run, and it is the exact failure this project is about -- an
# instrument that is confidently wrong about an input it mis-identified.
COMPOUND_RE = re.compile(rf"(?:{NUM}){SEP}(?:{UNIT_ALT})\b{SEP}$", re.IGNORECASE)


FRACTION_RE = re.compile(r"/\s*$")


def is_compound(text, match_start):
    """True if the match is only a FRAGMENT of the quantity actually written.

    Two ways that happens, both found by hand-checking flags rather than by
    thinking about it:
      "6 ft 3 inch (1.91 m)"    -> matching from '3 inch' reports a 25x error
      "1 1/8 in thick (28 mm)"  -> matching from '8 in' reports a 7x error
    Both are correct on the page. Both would have been my only 'findings'.
    """
    pre = text[max(0, match_start - 30):match_start]
    return bool(COMPOUND_RE.search(pre) or FRACTION_RE.search(pre))


def parse_num(s):
    return Decimal(s.replace(",", ""))


def rounding_interval(s):
    """The half-open interval a printed decimal literal stands for.

    "8"     -> [7.5, 8.5)      "8.0" -> [7.95, 8.05)
    "1,200" -> [1199.5, 1200.5)   -- I do NOT assume trailing zeros are
                                     insignificant; that would silently widen
                                     every round number and hide real errors.
    """
    t = s.replace(",", "")
    if "." in t:
        places = len(t.split(".")[1])
        half = Decimal(1).scaleb(-places) / 2
    else:
        half = Decimal("0.5")
    v = Decimal(t)
    return v - half, v + half


# A person who writes "70 mph (110 km/h)" has not made a mistake in the sense I
# am hunting. 70 mph is 112.7 km/h; they rounded to a comfortable number and
# every reader understands that. A person who writes "5 miles (80 km)" has done
# something categorically different. A strict interval test cannot tell those
# apart, so it does not get to call both of them errors.
#
#   CLOSES    the rounding intervals overlap. Nothing to see.
#   LOOSE     they don't overlap, but the printed value is within 10% of true.
#             Deliberate rounding, house style, or mild sloppiness. NOT counted.
#   MISMATCH  off by more than 10%. This is the population of interest.
#
# The 10% line is a judgement and I am stating it rather than burying it. It is
# set to be generous to the writer: it excuses every "about a hundred" in the
# corpus, at the cost of missing real errors smaller than a tenth. That trade is
# deliberate -- a rate that is too low is a boring result, and a rate inflated by
# counting ordinary rounding as error would be a lie.
LOOSE_BAND = Decimal("0.10")


def check_pair(a_num, a_unit, b_num, b_unit):
    """Return (verdict, detail). Verdict in CLOSES / LOOSE / MISMATCH / SKIP."""
    au, bu = a_unit.lower(), b_unit.lower()
    if au not in UNITS or bu not in UNITS:
        return "SKIP", "unknown unit"
    dim_a, fa = UNITS[au]
    dim_b, fb = UNITS[bu]
    if dim_a != dim_b:
        return "SKIP", "different dimensions"
    if au == bu:
        return "SKIP", "same unit"

    a_lo, a_hi = rounding_interval(a_num)
    b_lo, b_hi = rounding_interval(b_num)
    c_lo = a_lo * fa / fb
    c_hi = a_hi * fa / fb
    if not (c_hi <= b_lo or c_lo >= b_hi):
        return "CLOSES", ""

    exact = parse_num(a_num) * fa / fb
    if not exact:
        return "SKIP", "zero"
    ratio = parse_num(b_num) / exact
    if abs(ratio - 1) <= LOOSE_BAND:
        return "LOOSE", ""

    # name the shape of the failure -- these are the signatures I am hunting
    shape = "other"
    # "10 km (16 miles)": they multiplied by 1.609 instead of dividing by it.
    backwards = parse_num(a_num) * fb / fa
    if abs(ratio - 10) < Decimal("0.5") or abs(ratio - Decimal("0.1")) < Decimal("0.005"):
        shape = "factor of ten"
    elif abs(ratio - 100) < 5 or abs(ratio - Decimal("0.01")) < Decimal("0.0005"):
        shape = "factor of a hundred"
    elif backwards and abs(parse_num(b_num) / backwards - 1) <= LOOSE_BAND:
        shape = "conversion applied backwards"
    elif abs(ratio - 2) < Decimal("0.1") or abs(ratio - Decimal("0.5")) < Decimal("0.025"):
        shape = "factor of two"
    return "MISMATCH", {
        "printed": f"{a_num} {a_unit} ({b_num} {b_unit})",
        "should_be": f"{exact:.6g}",
        "ratio_printed_over_true": f"{ratio:.4f}",
        "shape": shape,
    }


# ---------------------------------------------------------------- gates
def gates():
    """The detector must survive these before any verdict of its own counts."""
    cases = [
        # (a, aunit, b, bunit, expected)
        # -- must CLOSE: ordinary correct rounded conversions
        ("5", "miles", "8", "km", "CLOSES"),
        ("1", "mile", "1.6", "km", "CLOSES"),
        ("100", "ft", "30", "m", "CLOSES"),
        ("29,032", "ft", "8,849", "m", "CLOSES"),      # Everest
        ("1", "acre", "0.40", "ha", "CLOSES"),
        ("200", "lb", "91", "kg", "CLOSES"),
        # -- must be LOOSE: a person rounding to a comfortable number.
        #    NOT an error. 70 mph is 112.7 km/h; I wrote this case expecting
        #    it to CLOSE and the detector was right and I was wrong, which is
        #    the reason the LOOSE band exists at all.
        ("70", "mph", "110", "km/h", "LOOSE"),
        ("26.2", "miles", "42", "km", "CLOSES"),   # and again: 42 km IS right
        # -- must be MISMATCH: the failure modes I am hunting
        ("5", "miles", "80", "km", "MISMATCH"),        # factor of ten
        ("5", "miles", "0.8", "km", "MISMATCH"),       # factor of ten, other way
        ("100", "ft", "300", "m", "MISMATCH"),         # decimal point moved
        ("10", "km", "16", "miles", "MISMATCH"),       # factor applied backwards
        ("124.77", "in", "0.9627", "m", "MISMATCH"),   # tonight's shape, in units
        ("50", "kg", "50", "lb", "MISMATCH"),          # 50 kg is 110 lb
        # -- must SKIP, not crash
        ("5", "miles", "8", "kg", "SKIP"),
    ]
    print("── gates (the detector must pass these before it may accuse anyone) ──")
    ok = True
    for a, au, b, bu, want in cases:
        got, detail = check_pair(a, au, b, bu)
        good = got == want
        ok &= good
        shape = f"  [{detail['shape']}]" if isinstance(detail, dict) else ""
        print(f"  [{'PASS' if good else 'FAIL'}] {a} {au} ({b} {bu}) -> "
              f"{got:8s} want {want}{shape}")
    print()
    return ok


