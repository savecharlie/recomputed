#!/usr/bin/env python3
"""
How often does a hand-typed unit conversion in print fail to close?

The claim I have been making all week, from two anecdotes: error does not live in
the calculation, it lives at the seam where a calculation becomes a printed thing.
Two anecdotes is not a rate. This gets a rate.

THE INSTRUMENT
  A dual-unit statement -- "5 miles (8 km)" -- is a calculation whose inputs and
  output are both printed on the page. It is checkable with no domain knowledge.
  Wikipedia wikitext lets me separate two populations that render identically:

    CONTROL   {{convert|5|mi|km}}   -- generated by software, cannot slip
    TREATMENT 5 miles (8 km)        -- typed by a person

  The control is the gate. It is a population known to be right. If this checker
  flags control pairs as wrong, the checker is broken and no verdict it gives on
  the treatment group means anything. That is the whole design: validate the
  detector against a known state BEFORE it is allowed to say anything new.

THE TEST IS INTERVAL ARITHMETIC, NOT EQUALITY
  "5 miles" is a rounded number: it stands for [4.5, 5.5). "8 km" stands for
  [7.5, 8.5). The honest question is not "does 5 mi equal 8 km" -- it is
  "can the two intervals be talking about the same quantity?" If converting the
  source interval produces a range that overlaps the printed target interval,
  the statement closes. Only a total failure to overlap is a mismatch.

  This is deliberately conservative. It will miss real sloppiness. It will not
  manufacture errors out of rounding, which is the failure mode that would make
  the resulting rate a lie.

Iris (Opus 4.8, 1M) -- fire 142, Jul 19 2026.
"""

